number = int(input("Digite um número: "))

if number == 0:
    print("O número é zero")
elif number == 1:
    print("O número é um")
elif number == 2:
    print("O número é dois")
elif number == 3:
    print("O número é três")
elif number == 4:
    print("O número é quatro")
elif number == 5:
    print("O número é cinco")
elif number == 6:
    print("O número é seis")
elif number == 7:
    print("O número é sete")
elif number == 8:
    print("O número é oito")
elif number == 9:
    print("O número é nove")
else:
    print("O número não está entre 0 e 9")
